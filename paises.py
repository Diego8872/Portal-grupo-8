PAISES_INV = {v: k for k, v in PAISES.items()}


def nombre_a_codigo(nombre):
    nombre_upper = nombre.upper().strip()
    if nombre_upper in PAISES:
        return PAISES[nombre_upper]
    for pais, codigo in PAISES.items():
        if nombre_upper in pais or pais in nombre_upper:
            return codigo
    return ""


def codigo_a_nombre(codigo):
    return PAISES_INV.get(str(codigo).zfill(3), f"Código {codigo}")
